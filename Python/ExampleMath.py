# ALWAYS import the math library to utilize certain mathematical calculations
import math

# user input of a positive numerical value:
y = float(input("Input a positive numerical value: "))

# Square Root - math.sqrt(value)
print("Square root of ", y, "is:")
sqrt_value = math.sqrt(y)
print(sqrt_value)



# user input of a negative numerical value:
z = float(input("Input a negative numerical value: "))

# Absolute Value - abs(value)
abs_value = abs(z)
print(abs_value)