# type: string
# Any inputs will work except for blanks

sInput = input("Input any string: ")
print(f"You entered the following string: {sInput}")

# type: int
# Attempting to input any decimal numbers or characters that is not a number (0, 1, 2, etc..) will result in an error.
# Any blank inputs will also result in error.

intInput = int(input("Input any integer: "))
print(f"You entered the following integer: {intInput}")

# type: float
# Attempting to input any numerical input will result in an error
# Any blank inputs will also result in error.

floatInput = float(input("Input any numerical value: "))
print(f"You entered the following numerical float value: {floatInput}")

